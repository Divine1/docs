const moment  = require("moment");
let starttime = "2020-10-21T00:00:00.000Z";
let timeframes=[];
let endttime=null;
for(let begin=0;begin<48;begin++){
    //endttime = moment(starttime).add(00,"hours").add(59,"minutes").add(59,"seconds").add(999,"milliseconds");
    endttime = moment(starttime).add(00,"hours").add(29,"minutes").add(59,"seconds").add(999,"milliseconds");
    timeframes.push({gt : starttime,lt : endttime.toISOString() })
    starttime = endttime.add(1,"milliseconds").toISOString();
}

console.log("timeframes ",timeframes)
console.log("timeframes.length ",timeframes.length)

